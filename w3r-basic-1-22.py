# Code:
def countFour(lst):
  count = 0
  for i in range(len(lst)):
    if lst[i] == 4:
      count += 1
  return count

print(countFour([1, 4, 6, 7, 4]))
print(countFour([1, 4, 6, 4, 7, 4]))

